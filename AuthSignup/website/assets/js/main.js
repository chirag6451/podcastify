// Smooth scrolling for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

// Navbar background change on scroll
window.addEventListener('scroll', function() {
    const navbar = document.querySelector('.navbar');
    if (window.scrollY > 50) {
        navbar.classList.add('navbar-scrolled');
    } else {
        navbar.classList.remove('navbar-scrolled');
    }
});

// Sample podcast player initialization
document.addEventListener('DOMContentLoaded', function() {
    // Initialize audio player (placeholder)
    const audioPlayer = document.querySelector('.audio-player');
    if (audioPlayer) {
        audioPlayer.innerHTML = `
            <div class="d-flex align-items-center">
                <button class="btn btn-primary me-3">
                    <i class="fas fa-play"></i>
                </button>
                <div class="flex-grow-1">
                    <div class="fw-bold">Sample AI Podcast</div>
                    <div class="text-muted">Generated by AI - 3:45</div>
                </div>
            </div>
        `;
    }

    // Initialize video grid (placeholder)
    const videoGrid = document.querySelector('.video-grid');
    if (videoGrid) {
        const videos = [
            { title: 'Tech Trends 2025', duration: '5:20' },
            { title: 'Future of AI', duration: '4:15' },
            { title: 'Digital Marketing', duration: '6:30' }
        ];

        videoGrid.innerHTML = videos.map(video => `
            <div class="video-item position-relative">
                <div class="ratio ratio-16x9 rounded overflow-hidden">
                    <div class="bg-dark d-flex align-items-center justify-content-center">
                        <i class="fas fa-play fa-2x text-white"></i>
                    </div>
                </div>
                <div class="mt-2">
                    <div class="fw-bold">${video.title}</div>
                    <div class="text-muted">${video.duration}</div>
                </div>
            </div>
        `).join('');
    }

    // Initialize platform icons
    const platformIcons = document.querySelector('.platform-icons');
    if (platformIcons) {
        const platforms = [
            'YouTube', 'Spotify', 'Apple Podcasts', 
            'Google Podcasts', 'Amazon Music'
        ];

        platformIcons.innerHTML = platforms.map(platform => `
            <div class="platform-icon">
                <i class="fab fa-${platform.toLowerCase().replace(' ', '-')} fa-2x"></i>
                <div class="small mt-2">${platform}</div>
            </div>
        `).join('');
    }
});

// Animation on scroll
const observerOptions = {
    threshold: 0.1
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('animated');
        }
    });
}, observerOptions);

document.querySelectorAll('.card, .platform-icon').forEach(el => {
    observer.observe(el);
});
